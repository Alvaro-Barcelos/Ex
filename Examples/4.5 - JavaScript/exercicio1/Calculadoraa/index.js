let numeroUm = parseFloat(prompt("Informe um numero:"))
let numeroDois = parseFloat(prompt("Informe outro numero:"))

const soma = numeroUm + numeroDois
const subtracao = numeroUm - numeroDois 
const multiplicacao = numeroUm * numeroDois 
const divisao = numeroUm / numeroDois

alert(`Adição: ${soma}\nSubtração: ${subtracao}\nMultiplicação: ${multiplicacao}\nDivisão: ${divisao}`)